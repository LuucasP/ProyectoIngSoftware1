package bean;


public class Problemas {
String idprob,nom_prob;

    public Problemas(String idprob, String nom_prob) {
        this.idprob = idprob;
        this.nom_prob = nom_prob;
    }

    public Problemas() {
    }

    public String getIdprob() {
        return idprob;
    }

    public void setIdprob(String idprob) {
        this.idprob = idprob;
    }

    public String getNom_prob() {
        return nom_prob;
    }

    public void setNom_prob(String nom_prob) {
        this.nom_prob = nom_prob;
    }

   


    
}
